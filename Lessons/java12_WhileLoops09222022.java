package Lessons;

public class java12_WhileLoops09222022
{
    public static void main(String[] args)
    {
        int a = 0; //starting point

        while (a<10) //this is any condition
        {
            System.out.println("Print this 10 times");
            a++; //this is the increment, it will increase the value of a by 1 in each iteration
        }


    }
}
