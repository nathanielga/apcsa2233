public class variables
{
    public static void main(String[] args) {
        //DECLARATION OF VARIABLES
        //  Datatype VariableName = value;
            String name = "Nathaniel";   // Text
            int myNum = 1738;            // Integer (whole number)
            float myFloatNum = 9.99f;    // Floating point number
            char myLetter = 'N';         // Character
            boolean myBool = true;       // Boolean
            byte smallNumber = 2;        // Stores whole numbers from -128 to 127
            short shortNumber = 4;       // Stores whole numbers from -32,768 to 32,767
            long longNumber = 123456789; // Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
            double doubleNumber = 12.45; // Stores fractional numbers. Sufficient for storing 15 decimal digits
            // RULES FOR VARIABLES AND DATA TYPES

            // You can not give reserved names for a variable like class etc...
            // It should not start from a number
            // Start always with lowercase, you can start with an uppercase, but it is not preferred
            // primitives are byte, short, long, int, float, double, char and boolean
            // Float ends with f
            // Double ends with d, but it is not required to work
            // Char has only one character within single quote
            // Long ends with l, but it is not required to work
            // Boolean only has true or false as value
            // Varibale Name Rules
            // 1. MUST be started with letters
            // 2. MUST be unique
            // 3. cannot be java reserved words
            // 4. cannot contain special characters $ and other primitive types


        // ... myNumber = 9;
        // ... myFloatNum = 8.99f;
        // ... myLetter2 = 'T';
        // ... myBool = false;
        // ... myText = "Hello World";

        int myNumber = 9;
        float myFloatNum2 = 8.99f;
        char myLetter2 = 'T';
        boolean myBool2 = false;
        String myText = "Hello World";
    }
}
